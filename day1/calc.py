#calculator program
def add(x,y):
    return x+y
def sub(x,y):
    return x-y
def mul(x,y):
    return x*y
def div(x,y):
    return x/y
def modulo(x,y):
    return x%y
print('sum=',add(5,6))
print("difference=",sub(20,10))
print("product=",mul(5,6))
print("quotient=",div(5,2))
print("remainder=",modulo(5,2))
